package life.jlu.osgi.packagetool.model;

public class Package {

    /* ----------------------------------------------------------------------------------------- */
    /* ----- Object Attributes ----------------------------------------------------------------- */
    /* ----------------------------------------------------------------------------------------- */
    private final String fqn;


    /* ----------------------------------------------------------------------------------------- */
    /* ----- Constructors ---------------------------------------------------------------------- */
    /* ----------------------------------------------------------------------------------------- */
    /**
     * Creates a new instance of the class Package.
     * @param fqn Fully-qualified name of the package
     */
    public Package(String fqn) {
        this.fqn = fqn;
    }


    /* ----------------------------------------------------------------------------------------- */
    /* ----- Object Methods -------------------------------------------------------------------- */
    /* ----------------------------------------------------------------------------------------- */
    /**
     * Returns the fully-qualified name of the package.
     * @return Fully-qualified name
     */
    public String getFqn() {
        return fqn;
    }

    @Override
    public String toString() {
        return fqn;
    }
}
